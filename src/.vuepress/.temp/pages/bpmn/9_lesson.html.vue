<h3 id="что-хорошего-в-этои-схеме" tabindex="-1"><a class="header-anchor" href="#что-хорошего-в-этои-схеме"><span>Что хорошего в этой схеме</span></a></h3>
<p>В целом, из схемы понятно, что хотел сказать автор — инициативу по закупке может подавать любой сотрудник, она обязательно проходит согласование с генеральным директором, который должен выбрать с каким поставщиком работать.</p>
<p>После этого получаем счёт от него, еще раз согласовываем счёт, и получаем закрывающие документы в бухгалтерию.</p>
<p>Еще хорошо:</p>
<ul>
<li>счастливый путь понятен;</li>
<li>наименование задач в формате «Глагол» + «Существительное».</li>
</ul>
<h3 id="что-плохого-в-этои-схеме" tabindex="-1"><a class="header-anchor" href="#что-плохого-в-этои-схеме"><span>Что плохого в этой схеме</span></a></h3>
<ul>
<li>использовать Milestone (прерывистые линии и надписи сверху) для обозначения ролей, выполняющих задачи нельзя — это дополнительная «фишка» Bizagi, такой штуки в BPMN2.0 нет;</li>
<li>шумно — много линий, создается впечатление чего-то запутанного, хотя процесс описан просто;</li>
