<h2 id="бизнес-процесс-«согласование-счета»" tabindex="-1"><a class="header-anchor" href="#бизнес-процесс-«согласование-счета»"><span>Бизнес-процесс «Согласование счёта»</span></a></h2>
<p>Самый частый случай из обслуживающих процессов — оплата счёта. Кто-то в интересах компании хочет что-то купить. Начинается такой процесс с подачи потребности, а заканчивается либо отказом, либо полученными документами в бухгалтерии.</p>
