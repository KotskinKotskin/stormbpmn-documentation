<h2 id="что-меняем-в-схеме" tabindex="-1"><a class="header-anchor" href="#что-меняем-в-схеме"><span>Что меняем в схеме</span></a></h2>
<p>Генеральный директор не хочет согласовывать счета по одному — и он прав, у него полно другой полезной работы.</p>
<p>Изменения:</p>
<ul>
<li>поставщиков и варианты покупки ГД согласует по одиночке, а счета оплачивает скопом;</li>
<li>добавим шлюз после запроса к поставщику счёта;</li>
<li>изменим формулировку задачи с платёжкой;</li>
<li>добавим развилку, если сумма счёта небольшая;</li>
<li>изменим формулировку о платёжке.</li>
</ul>
