<h2 id="вопросы-к-аналитику-и-бизнесу" tabindex="-1"><a class="header-anchor" href="#вопросы-к-аналитику-и-бизнесу"><span>Вопросы к аналитику и бизнесу</span></a></h2>
<p>Самый главный вопрос — правда, что генеральный директор согласовывает счета и предложения по покупке по одному запросу? А если счетов 50 штук в день?
В оплате счетов генеральные директора, финансовые службы, бухгалтерия оплачивают и согласовывают счета пачками.</p>
<p>Еще небольшие вопросы:</p>
<ul>
<li>что будет с процессом, если поставщик не прислал счёт? На внешних участников мы воздействовать не можем, можем только реагировать. А сейчас процесс в любом случае требуется получения счёта;</li>
<li>платёжка — это сразу электронный документ. Странно что его нужно сканировать, да и еще делает этот тот же самый человек, что и отправляет поставщику;</li>
<li>странно, что любой сотрудник может подать запрос и он не согласовывается с руководителем инициатора;</li>
<li>странно, что нет согласований и сравнений с бюджетами. Неужели я могу в середине месяца подать запрос на 1.5 млн.рублей?</li>
<li>что произойдет, если потребность одинаковая, например купить ручки. Будет 2 счета на 100 рублей, или соберём один на 200 и пустим на согласование?</li>
<li>как быть с регулярными платежами? Они будут запускаться по этому процессу или будут оплачиваться как-то иначе?</li>
<li>как быть, если потребность на небольшую сумму, рублей на 500? Прогонять весь процесс может быть дорого;</li>
<li>обязательно ли всем поставщикам отправлять платёжку? А если она кому-то не нужна?</li>
</ul>
<p>Нагруженный этими вопросами начинающий аналитик начинает думать, вырабатывает решения и идет к клиенту их согласовывать.</p>
