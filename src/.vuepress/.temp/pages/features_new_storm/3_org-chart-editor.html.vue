<template><div><h1 id="редактор-оргструктуры" tabindex="-1"><a class="header-anchor" href="#редактор-оргструктуры"><span>Редактор оргструктуры</span></a></h1>
<p>Stormbpmn позволяет моделировать оргструктуру предприятия, соединять ее с ролями на бизнес-процессах и конкретными сотрудниками. Такая связь может однозначно ответить, что именно делает конкретная должность или даже сотрудник в бизнес-процессах.<br>
<img src="@source/features_new_storm/orgchart-1.png" alt="image"></p>
<p>Решение такой задачи базируется на нескольких тезисах:</p>
<ul>
<li>У задачи может быть только 1 роль</li>
<li>Должность может выполнять сколько угодно ролей</li>
<li>Роль может выполняться каким угодно количеством должностей</li>
<li>Сотрудник может быть назначен на любое количество должностей</li>
<li>Сотрудник не может быть назначен на роль напрямую</li>
</ul>
<div class="hint-container warning">
<p class="hint-container-title">Примечание</p>
<p>Оргструктура работает на подписке TEAM и выше.</p>
</div>
<p><img src="@source/features_new_storm/orgchart-2.png" alt="image"></p>
