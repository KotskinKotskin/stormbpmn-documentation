<ul>
<li>Скачать оргструктуру в .PNG.</li>
<li>Выровнять оргструктуру.</li>
<li>Найти на схеме конкретную должность.</li>
<li>Найти на схеме должности, участвующие в конкретном процессе.</li>
</ul>
