<li>Посмотреть, сколько человек назначено на должность.</li>
<li>Открыть редактирование карточки.</li>
<li>Удалить карточку.</li>
<li>Добавить подчинённый элемент.</li>
<li>Присоединить подчинённый элемент.</li>
</ol>
