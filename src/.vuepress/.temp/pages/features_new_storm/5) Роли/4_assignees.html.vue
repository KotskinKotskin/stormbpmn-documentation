<p>Роли это отличный инструмент для указания исполнителей задач. Под ролью понимается исполнитель конкретной задачи в процессе или процессах (т.к. Роли - это справочник, который можно переиспользовать). Роли это правильная и хорошая альтернатива дорожкам.</p>
<p>Роль - это испольнитель задачи. Роль это не должность.
1 задача = 1 роль = N (несколько) должностей.
Одну роль может выполнять несколько должностей. Например, роль &quot;Инициатор закупки товаров для нужд компании&quot;. Её может выполнять несколько должностей в одной компании: офис-менеджер, бухгалтер, заведующий хозяйством, администратор
А может и одна должность выполнять несколько ролей. Например, директор компании может выполнять роль, исполняющую административные задачи, а также роль, выполняющую юридические задачи, функции ведения бухгалтерского учёта, кадровую работу и т.д</p>
