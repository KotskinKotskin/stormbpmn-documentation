</div>
<h3 id="приглашение-в-команду" tabindex="-1"><a class="header-anchor" href="#приглашение-в-команду"><span>Приглашение в команду</span></a></h3>
<p>Чтобы команда работала, люди должны иметь уточные записи в системе и вступить в команду. Есть несколько способов вступить в команду:</p>
<h4 id="по-почте" tabindex="-1"><a class="header-anchor" href="#по-почте"><span>По почте</span></a></h4>
<p>При создании команды или по <a href="https://stormbpmn.com/app/team/" target="_blank" rel="noopener noreferrer">ссылке<ExternalLinkIcon/></a> при нажатии на кнопку <strong>&quot;пригласить по почте&quot;</strong> вы можете указать почтовые ящики ващих коллег. Если у них есть аккаунт в системе и они не входят в команды, от они сразу же будут добавлены в вашу команду. Если у них нет аккаунтов, то сначала они получат приглашение зарегистрироваться.</p>
<div class="hint-container caution">
<p class="hint-container-title">Предупреждение</p>
<p>Поскольку приглашение по почте связано с отправкой писем от нашего имени, у нас встроена защита от спама. Не отправляйте больше 5 приглашений за раз.</p>
</div>
<h4 id="по-ссылке" tabindex="-1"><a class="header-anchor" href="#по-ссылке"><span>По ссылке</span></a></h4>
<p>Самый удобный вариант - получить ссылку по кнопке <strong>&quot;пригласить по ссылке&quot;</strong> и отправить его коллегам. Перейдя по ней они смогут зарегистрироваться или присоедениться к команде. В этом случае у них будут права на чтение, их можно будет сменить позднее.</p>
<div class="hint-container warning">
<p class="hint-container-title">Примечание</p>
<p>Если участник уже член команды, то вступить по ссылке у него не получится - сначала надо выйти из команды или удалить команду.</p>
</div>
<h4 id="по-домену" tabindex="-1"><a class="header-anchor" href="#по-домену"><span>По домену</span></a></h4>
<p>Если ваша организация имеет свой домен, например <strong>stormbpmn.com</strong>  , то вы можете указать его в настройках команды. Тогдай любой человек с такой почтой, например <strong>d.kotov@stormbpmn.com</strong>, получат возможность вступить в вашу команду.  Тоже хороший способ собрать всех коллег вместе. Указываете домен и говорите &quot;Регистрируемся с рабочих почт и вступаем в команду!&quot;.</p>
<h3 id="управление-правами" tabindex="-1"><a class="header-anchor" href="#управление-правами"><span>Управление правами</span></a></h3>
<p>Сейчас существует 3 типа прав:</p>
<ul>
<li><strong>Администратор</strong> - может создавать роли и элементы архитектуры и делать вообще все с командой.</li>
<li><strong>Чтение\редактирование</strong> - НЕ может создавать роли и элементы архитектуры, НЕ может менять настройки команды. Может редактировать папки и диаграммы коллег.</li>
<li><strong>Чтение</strong> - Не может редактировать диаграммы коллег, не может трогать папки. Может создавать диаграммы.</li>
</ul>
<p>Управляение правами осуществяется путем выбора участников и нажатия на кнопку <strong>&quot;Сменить права&quot;</strong>.</p>
