</div>
<h3 id="приглашение-в-команду" tabindex="-1"><a class="header-anchor" href="#приглашение-в-команду"><span>Приглашение в команду</span></a></h3>
<p>Чтобы команда работала, люди должны иметь учетные записи в системе и вступить в команду. Есть несколько способов вступить в команду:</p>
<h4 id="по-почте" tabindex="-1"><a class="header-anchor" href="#по-почте"><span>По почте</span></a></h4>
<p>При создании команды или по <a href="https://stormbpmn.com/app/team/participants" target="_blank" rel="noopener noreferrer">ссылке<ExternalLinkIcon/></a> при нажатии на кнопку <strong>&quot;пригласить по почте&quot;</strong> вы можете указать почтовые ящики ващих коллег. Если у них есть аккаунт в системе и они не входят в команды, от они сразу же будут добавлены в вашу команду. Если у них нет аккаунтов, то сначала они получат приглашение зарегистрироваться.
<img src="@source/team-work/invite_team.png" alt="image"></p>
<div class="hint-container caution">
<p class="hint-container-title">Предупреждение</p>
<p>Поскольку приглашение по почте связано с отправкой писем от нашего имени, у нас встроена защита от спама. Не отправляйте больше 5 приглашений за раз.</p>
</div>
<h4 id="по-ссылке" tabindex="-1"><a class="header-anchor" href="#по-ссылке"><span>По ссылке</span></a></h4>
<p>Самый удобный вариант - получить ссылку по кнопке <strong>&quot;Пригласить по ссылке&quot;</strong> и отправить его коллегам удобным Вам способом. Перейдя по ней они смогут зарегистрироваться или присоедениться к команде. В этом случае у них будут права на чтение, их можно будет сменить позднее.</p>
<h4 id="по-домену" tabindex="-1"><a class="header-anchor" href="#по-домену"><span>По домену</span></a></h4>
<p>Если ваша организация имеет свой домен, например <strong>stormbpmn.com</strong>  , то вы можете указать его в настройках команды. Тогдай любой человек с такой почтой, например <strong>d.kotov@stormbpmn.com</strong>, получат возможность вступить в вашу команду.  Тоже хороший способ собрать всех коллег вместе. Указываете домен и говорите &quot;Регистрируемся с рабочих почт и вступаем в команду!&quot;.</p>
<h3 id="управление-правами" tabindex="-1"><a class="header-anchor" href="#управление-правами"><span>Управление правами</span></a></h3>
<p>В системе у участников команды есть базовые права 3 типов:</p>
<ol>
<li><strong>Админ</strong> - права управления командой и её объектами: пр роли, элементы архитектуры (создание, редактирование, удаление).</li>
<li><strong>Чтение и изменение</strong> - НЕ может создавать роли и элементы архитектуры, НЕ может менять настройки команды. Может редактировать папки и диаграммы коллег.</li>
<li><strong>Только чтение</strong> - Не может редактировать диаграммы коллег, не может трогать папки. Может создавать диаграммы.</li>
</ol>
<p>Управление правами осуществяется путем выбора участника из списка команды и нажатия на кнопку <strong>&quot;Изменить права&quot;</strong>.<br>
<img src="@source/team-work/change_team_rights.png" alt="image"></p>
