<p>Командная работа в система в работе предназначена для того, чтобы экономить ваше время за счет:</p>
<ul>
<li>Все видят одни и теже процессы</li>
<li>Все процессы в актуальном состоянии</li>
<li>Не надо пересылать файлы и ссылки</li>
<li>Все изменения версионируются и можно откатиться к пред. версиям, если кто-то что-то сломал</li>
<li>Можно посмотреть, чем текущая версия отличается от предыдущей.</li>
</ul>
<h2 id="_4-типа-команднои-работы" tabindex="-1"><a class="header-anchor" href="#_4-типа-команднои-работы"><span>4 типа командной работы</span></a></h2>
<p>В системе существует несколько способов поделиться своей работой с коллегами:</p>
<ol>
<li>на уровне процесса;</li>
<li>на уровне команды;</li>
<li>на уровне предприятия.</li>
</ol>
<p>Чем ближе выданы права к диаграмме, тем они сильнее. Т.е. права на уровне процесса сильнее прав на уровне папки. А на уровне папки - сильнее чем на уровне команды.</p>
<h3 id="на-уровне-процесса-поделиться-конкретнои-диаграммои" tabindex="-1"><a class="header-anchor" href="#на-уровне-процесса-поделиться-конкретнои-диаграммои"><span>На уровне процесса - поделиться конкретной диаграммой</span></a></h3>
<p>Это способ работает когда нужно просто поделиться одной диаграммой. Он описан в функции <RouteLink to="/features_new_storm/1_bpmn-editor/#%D0%BF%D0%BE%D0%B4%D0%B5%D0%BB%D0%B8%D1%82%D1%8C%D1%81%D1%8F-%D0%B4%D0%B8%D0%B0%D0%B3%D1%80%D0%B0%D0%BC%D0%BC%D0%BE%D0%B8">Поделиться диаграммой</RouteLink></p>
<div class="hint-container tip">
<p class="hint-container-title">Совет</p>
<p>Этот вариант хорошо работает, когда вы не хотите приглашать человека в вашу команду, но вам нужно поделиться с ним группой каких-то процессов.</p>
</div>
<h2 id="управление-командои" tabindex="-1"><a class="header-anchor" href="#управление-командои"><span>Управление командой</span></a></h2>
<p>Команда - это группа людей, которые вместе работают над одними процессами. <em>Например, у вас есть команда &quot;Кредитные карты&quot;, в которой 10 человек и все они работают с продуктом &quot;Кредитная карта&quot;</em>.<br>
Участники команды, в отличии от всех других способов совместной работы, получают такие приемущества:</p>
<ol>
<li><strong>Видят все диаграммы всех участников команды автоматически</strong>, без необходимости предоставлять права</li>
<li>Видят структуру папок</li>
<li>Работают по общим требованиям, правилам качества описания процессов</li>
<li>У них общие:
<ul>
<li><RouteLink to="/team-work/3_org-chart-editor.html">оргструктура</RouteLink></li>
<li><RouteLink to="/team-work/4_assignees.html">роли</RouteLink></li>
<li><RouteLink to="/team-work/5_elements-architecture/">элементы архитектуры</RouteLink></li>
<li><RouteLink to="/team-work/6_reglaments/process_reglament.html#%D1%88%D0%B0%D0%B1%D0%BB%D0%BE%D0%BD-%D1%80%D0%B5%D0%B3%D0%BB%D0%B0%D0%BC%D0%B5%D0%BD%D1%82%D0%B0-%D0%BF%D1%80%D0%BE%D1%86%D0%B5%D1%81%D1%81%D0%B0">шаблоны регламентов</RouteLink></li>
</ul>
</li>
</ol>
<div class="hint-container caution">
<p class="hint-container-title">Предупреждение</p>
<p>Доступность диаграмм для членов команды определяется по ее участникам. Участник входит в команду - его диаграммы всем видны. Участник уволился и его убрали из команды - диаграммы не видны.<br>
Чтобы <strong>избежать недоступности диаграмм после увольнения сотрудников</strong> используйте кнопку <a href="#%D1%81%D0%B4%D0%B5%D0%BB%D0%B0%D1%82%D1%8C-%D1%81%D0%B5%D0%B1%D1%8F-%D0%B0%D0%B2%D1%82%D0%BE%D1%80%D0%BE%D0%BC-%D0%BF%D1%80%D0%BE%D1%86%D0%B5%D1%81%D1%81%D0%BE%D0%B2">&quot;Сделать себя автором процессов&quot;</a>  перед тем, как удалить участника.</p>
</div>
